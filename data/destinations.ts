// Quick lookup map
export const destinationMap = new Map(
  popularDestinations.map((d) => [d.slug, d])
);

export function getDestination(slug: string): Destination | undefined {
  return destinationMap.get(slug);
}

export function getDestinationsByRegion(region: string): Destination[] {
  return popularDestinations.filter((d) => d.region === region);
}

export function searchDestinations(query: string): Destination[] {
  const q = query.toLowerCase();
  return popularDestinations.filter(
    (d) =>
      d.name.toLowerCase().includes(q) ||
      d.country.toLowerCase().includes(q) ||
      d.region.toLowerCase().includes(q) ||
      d.description.toLowerCase().includes(q) ||
      d.tags.some((t) => t.toLowerCase().includes(q))
  );
}

export function getFeaturedDestinations(): Destination[] {
  return popularDestinations.filter((d) => d.popularity >= 8).slice(0, 6);
}
